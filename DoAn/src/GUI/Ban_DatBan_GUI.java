    public void laydulieuBan(ArrayList<Ban> dsb){
        tblData.removeAllElements();
        for(Ban b : dsb){
            Vector v = new Vector();
            v.add(b.getMaBan()); v.add(b.getTenBan()); v.add(b.getSoGhe());v.add(b.getTrangThai());
            tblData.add(v);
        }
        
        tbB.setModel(new DefaultTableModel(tblData, tblTitl3e));
    }
    
    public void bangDB(){
        tblTitleDatBan.add("Mã Đặt bàn"); tblTitleDatBan.add("Mã Bàn"); tblTitleDatBan.add("Mã NV"); tblTitleDatBan.add("Thời gian"); 
        tblTitleDatBan.add("Mã KH"); tblTitleDatBan.add("Tình trạng"); tblTitleDatBan.add("Số lượng khách"); 
    }
    public void laydulieuDatBan(ArrayList<Ban_DatBanPOJO> dsb){
        tblDataDatBan.removeAllElements();
        for(Ban_DatBanPOJO b : dsb){
            Vector v = new Vector();
            v.add(b.getMadatban()); v.add(b.getMaban()); v.add(b.getManv());v.add(b.getThoigian()); v.add(b.getMakhachhang());v.add(b.getTinhtrang());v.add(b.getSoluongkhach());
            tblDataDatBan.add(v);
        }
        
        tbDB.setModel(new DefaultTableModel(tblDataDatBan, tblTitleDatBan));
    }
