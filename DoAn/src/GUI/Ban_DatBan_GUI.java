    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
       if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn thêm không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                String sql = "insert into DatBan(MaBan, MaNV, ThoiGian, MaKhachHang, SoLuongKhach, TinhTrang) values ('"+txtmaban.getText().trim()+"', '"+txtmanv.getText().trim()+"', '"+txtthoigian.getText().trim()+"', '"+txtmakh.getText().trim()+"', '"+txtsoluong.getText().trim()+"', N'"+txttinhtrang.getText().trim()+"')";
                int kq=Ban_DatBanDAO.ThemXoaSuaDatBan(sql);
                if(kq!=-1)
                {
                    dsdb = Ban_DatBanDAO.getDSDB();
                    laydulieuDatBan(dsdb );
                    dsb = Ban_DatBanDAO.getDSB();
                    laydulieuBan(dsb );
                }
                else
                JOptionPane.showMessageDialog(this, "Mã đặt phòng này đã tồn tại, không thể thêm", "Thông báo", 2);

            }
    }//GEN-LAST:event_btnThemActionPerformed

