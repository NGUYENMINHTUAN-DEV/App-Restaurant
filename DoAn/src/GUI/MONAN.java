    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
       if (txtTen.getText().isEmpty() || txtSoLuong.getText().isEmpty() || tenHinh.isEmpty() || txtDonVi.getText().isEmpty() || txtGia.getText().isEmpty() || txtMoTa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
            return;
        }
        LoaiMon a = (LoaiMon) dcbm.getElementAt(cboLoai.getSelectedIndex());
        MonAn m = new MonAn();
        m.setMaLoai(a.getMaLoai());
        m.setTenMonAn(txtTen.getText());
        m.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        m.setHinh(tenHinh);
        m.setDonViTinh(txtDonVi.getText());
        try {
            m.setDonGia(NumberFormat.getInstance().parse(txtGia.getText()).intValue());
        } catch (ParseException e) {
            e.printStackTrace();
        }
        m.setMoTa(txtMoTa.getText());

        if (txtMa.getText().isEmpty()) {
            if (MonAnDAO.themCSDL(m)) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                emptyInput();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại");
            }
        } else {
            m.setMaMonAn(Integer.parseInt(txtMa.getText()));
            if (MonAnDAO.capNhatCSDL(m)) {
                JOptionPane.showMessageDialog(this, "Sửa thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Sửa thất bại");
            }
        }
        loadMonAn("");
        btnThem.setEnabled(true);
        btnLuu.setEnabled(false);
        btnChonHinh.setEnabled(false);
        txtEnable(false);
        tenHinh = "";
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnChonHinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonHinhActionPerformed
        // TODO add your handling code here:
        try {
            JFileChooser fileChooser = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("Images(*.png;*.jpg;*.jpeg;*.gif;*.webp)", "png", "jpg", "jpeg", "gif", "webp");
            fileChooser.addChoosableFileFilter(filter);
            fileChooser.setAcceptAllFileFilterUsed(false);
            int kt = fileChooser.showOpenDialog(null);
            if (kt == JFileChooser.APPROVE_OPTION) {
                File f = fileChooser.getSelectedFile();
                tenHinh = f.getName();
                String location = "src/Image/" + tenHinh;
                File destinationF = new File(location);

                File desF = new File(location);
                if (!desF.exists()) {
                    Files.copy(f.toPath(), destinationF.toPath());
                }

                ImageIcon hinh = new ImageIcon(new ImageIcon(location).getImage().getScaledInstance(lblHinh.getWidth(), lblHinh.getHeight(), Image.SCALE_DEFAULT));
                lblHinh.setIcon(hinh);
            }
        } catch (HeadlessException | IOException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnChonHinhActionPerformed

