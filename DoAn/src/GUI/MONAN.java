    private void loadMonAn(String key) {
        dtm.setColumnCount(0);
        Vector data = new Vector();
        tieuDe.add("Mã món ăn");
        tieuDe.add("Loại món");
        tieuDe.add("Tên món ăn");
        tieuDe.add("Số lượng");
        tieuDe.add("Đơn vị tính");
        tieuDe.add("Đơn giá");
        tieuDe.add("Mô tả");
        tieuDe.add("Hình");

        ArrayList<MonAn> dsMA = MonAnDAO.showMonAn(key);
        for (MonAn ma : dsMA) {
            Vector<Object> v = new Vector<>();
            v.add(ma.getMaMonAn());
            v.add(ma.getTenLoai());
            v.add(ma.getTenMonAn());
            v.add(ma.getSoLuong());
            v.add(ma.getDonViTinh());
            v.add(NumberFormat.getInstance().format(ma.getDonGia()));
            v.add(ma.getMoTa());
            v.add(ma.getHinh());
            data.add(v);
        }

        if (data != null) {
            dtm.setDataVector(data, tieuDe);
        }
        TableColumnModel tcm = jTable1.getColumnModel();
        tcm.removeColumn(tcm.getColumn(7));
    }

