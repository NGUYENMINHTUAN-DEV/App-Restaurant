    private void txtGiaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtGiaKeyReleased
        // TODO add your handling code here:
        String gia = txtGia.getText();
        if (!gia.isEmpty()) {
            try {
                txtGia.setText(NumberFormat.getInstance().format(NumberFormat.getInstance().parse(gia).doubleValue()));
            } catch (ParseException ex) {
                Logger.getLogger(MONAN.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_txtGiaKeyReleased

