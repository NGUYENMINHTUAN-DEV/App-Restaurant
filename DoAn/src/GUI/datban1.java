    private void btn_thanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_thanhToanActionPerformed
        try {
            // TODO add your handling code here:
            PhieuGoiMonDAO phieuGoiMonDAO = new PhieuGoiMonDAO();
            //TU CHOI THANH TOÁN NEU TIEN THUA AM
            int tienThua = Integer.parseInt(NumberFormat.getInstance().parse(this.label_tienThua.getText()) + "");
            if (tienThua < 0) {
                JOptionPane.showMessageDialog(this, "Tiền nhận không đủ", "Thông báo", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (ParseException ex) {
            Logger.getLogger(datban1.class.getName()).log(Level.SEVERE, null, ex);
        }

        //CAP NHAT TIEN THANH 0 DONG
        txt_tienNhanCuaKhach.setText("0");
        label_tienThua.setText("0 VNĐ");
        this.tienNhan = 0;

        //ĐỔI TRẠNG THÁI BÀN
        this.changeStateTable(false);

        //LƯU HÓA ĐƠN
        try {
            //TẠO HÓA DON VÀ INSERT VAO BẢNG HOA DON
            HOADONDAO hoaDonDao = new HOADONDAO();
            HoaDon hoaDon = new HoaDon();
            hoaDon.setTongTien(tongTien);
            hoaDon.setMaNhanVien(this.maNV);
            
            String tennv = label_tenNhanVien.getText();
            String maNV = NhanVienDAO.getNhanVienByMa(tennv);
            
            boolean n = hoaDonDao.themHoaDon(hoaDon, maNV);
            if (n) {
                //NHẬN MÃ HOA DON VỪA TẠO
                String maHoaDon = getLastInsertMaHoaDon();
                //INSERT BẢNG CHI TIET HOA DON
                createChiTietHoaDon(maHoaDon);
                //Xoa phieu goi
                phieuGoiMonDao.deleteAllPhieuGoiMonByMaBan(this.maBan);
                JOptionPane.showMessageDialog(this, "Thanh toán thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                loadTableMonAn();
            } else {
                JOptionPane.showMessageDialog(this, "Thanh toán thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        } catch (HeadlessException | NumberFormatException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Thanh toán thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_thanhToanActionPerformed

    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:

        //CHECK SỐ LƯỢNG ĐỒ ĂN COI CÒN ĐỂ GỌI KO
        if (this.cbo_monAn.getSelectedIndex() == -1) {
            return;
        }
        MonAnDAO monAnDao = new MonAnDAO();
        MonAn monAn = (MonAn) this.cbo_monAn.getSelectedItem();
        if (monAnDao.getSoLuongByMaMonAn(monAn.getMaMonAn() + "") < Integer.parseInt(txt_SoLuong.getText())) {
            JOptionPane.showMessageDialog(this, "Số lượng cho món ăn này không đủ !!!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        //THAY ĐỔI TRẠNG THÁI BÀN THÀNH ĐÃ CÓ KHÁCH
        this.changeStateTable(true); 

        if (this.findFootQuantityInPhieuGoiMon() != -1) {
            int n = JOptionPane.showConfirmDialog(this, "Món này đã được gọi, bạn có muốn thêm số lượng?", "Thông báo", JOptionPane.YES_NO_OPTION);
            if (n == JOptionPane.YES_OPTION) {
                this.updateTableMonAn();
            }
        } else {
            this.themMonAnTableMonAn();
        }
        loadTableMonAn();
    }//GEN-LAST:event_btn_addActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        if (this.table_monAn.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn món ăn!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int n = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa ?", "Thông báo", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (n == JOptionPane.NO_OPTION) {
            return;
        }
        int rowSelect = this.table_monAn.getSelectedRow();
        PhieuGoiMonDAO phieuGoiMonDao = new PhieuGoiMonDAO();
        PhieuGoiMon phieuGoiMon = new PhieuGoiMon(Integer.parseInt(this.table_monAn.getValueAt(rowSelect, 0).toString()), Integer.parseInt(this.maBan));
        if (!phieuGoiMonDao.xoaPhieuGoiMon(phieuGoiMon)) {
            JOptionPane.showMessageDialog(this, "Xóa thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
        this.btn_delete.setEnabled(false); 
        this.loadTableMonAn();
        if (!this.checkBanAnHasChecked(maBan)) {
            this.changeStateTable(false);
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

