    private void txt_tienNhanCuaKhachKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_tienNhanCuaKhachKeyReleased
        // TODO add your handling code here:
        if ((!Character.isDigit(evt.getKeyChar()) && !Character.isISOControl(evt.getKeyChar())) || this.tongTien == 0) {
            evt.consume();
        } else {
            //LUU LAI TIEN NHAN(FORMAT THEO CODE) VAO BIẾN TIEN NHAN
            if (!Character.isISOControl(evt.getKeyChar())) {
                this.tienNhan = Integer.parseInt(String.valueOf(this.tienNhan) + evt.getKeyChar());
            } else {
                this.tienNhan /= 10;
            }
            evt.consume();
            //XU LY TIEN THUA
            int tienThua = this.tienNhan - this.tongTien;
            this.label_tienThua.setText(NumberFormat.getInstance().format(tienThua) + " VNĐ");
            //NumberFormat.getInstance().parse(this.txt_tienNhanCuaKhach.getText()) (CHUYỂN ĐỔI STRING PHÂN TÁCH HÀNG NGÀN VỀ SỐ)
            //FOTMAT TIEN NHAN
            this.txt_tienNhanCuaKhach.setText(NumberFormat.getInstance().format(this.tienNhan));

            //CHECK ENABLE BTN THANH TOAN
            if (this.checkEnableBtnPay()) {
                this.btn_thanhToan.setEnabled(true);
            }
        }
    }//GEN-LAST:event_txt_tienNhanCuaKhachKeyReleased

