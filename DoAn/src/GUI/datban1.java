    private void loadTableMonAn() {
        try {
            
            this.tongTien = 0; 
            
            this.tableModel.setRowCount(0);
            ArrayList<PhieuGoiMon> dsPhieu = phieuGoiMonDao.getPhieuGoiMonByMaBan(this.maBan);
            for (PhieuGoiMon x : dsPhieu) {
                Vector<Object> vector = new Vector<>(); 
                vector.add(x.getMaMonAn());
                vector.add(x.getMaNhanVien());
                vector.add(x.getTenMonAn());
                vector.add(x.getSoLuong());
                vector.add(NumberFormat.getInstance().format(x.getDonGia()));
                this.tongTien += x.getSoLuong() * x.getDonGia();
                tableModel.addRow(vector);
            }

            this.table_monAn.updateUI();
            this.label_tongTien.setText(NumberFormat.getInstance().format(tongTien) + " VNĐ");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

