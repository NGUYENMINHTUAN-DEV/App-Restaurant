            x.setMaBan(Integer.parseInt(this.maBan));
            x.setMaNhanVien(this.maNV);
            x.setMaMonAn(monAn.getMaMonAn());
            x.setSoLuong(Integer.parseInt(txt_SoLuong.getText()) + soLuongMonAn);
