    private void hienThi() {
        ArrayList<POJO.HoaDon> dsHoaDon = HOADONDAO.layDanhSachHoaDon();
        dtmHD.setRowCount(0);
        for (POJO.HoaDon hd : dsHoaDon) {
            Vector<Object> v = new Vector<>();
            v.add(hd.getMaHoaDon());
            v.add(hd.getMaKhachHang());
            v.add(hd.getMaNhanVien());
            v.add(hd.getNgayThanhToan());
            v.add(hd.getTrangThai());
            v.add(hd.getTongTien());
            dtmHD.addRow(v);
        }
        tbDanhSachHD.setModel(dtmHD);
    }

    private void hienThiChiTiet(String ma) {
        ArrayList<POJO.ChiTietHoaDon> dsCTHoaDon = HOADONDAO.layDanhSachCTHoaDon(ma);
        dtmCTHD.setRowCount(0);
        for (POJO.ChiTietHoaDon cthd : dsCTHoaDon) {
            Vector<Object> v = new Vector<>();
            v.add(cthd.getMaHoaDon());
            v.add(cthd.getTenMonAn());

            //v.add(cthd.getMaMonAn());
            v.add(cthd.getSoLuong());
            dtmCTHD.addRow(v);
        }
        tbDanhSachCTHD.setModel(dtmCTHD);
    }
    
