    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:

        String ten = txtHoTen.getText().trim();
        String sdt = txtSDT.getText().trim();
        String taikhoan = txt_taikhoan.getText().trim();
        String matkhau = txt_matkhau.getText().trim();
        
        String ngaysinh = dcsNgaySinh.getText();
        String gioitinh;
        if (rdoNam.isSelected()) {
            gioitinh = "Nam";
        } else {
            gioitinh = "Nữ";
        }
        String dc = txtDiaChi.getText().trim();

        BoPhan bp = (BoPhan) dcmBP.getSelectedItem();
        String maBP =bp.getMaBP();

        String selectedItem = cbo_quyen.getItemAt(cbo_quyen.getSelectedIndex());
        if (!txtMaNV.getText().isEmpty()) {
            NhanVien nv = new NhanVien();
             nv.setMaNV(txtMaNV.getText());
            nv.setMaBP(maBP);
            nv.setHinh(tenHinh);
            nv.setHoTen(ten);
            nv.setNgaysinh(ngaysinh);
            nv.setSdt(sdt);
            nv.setGioitinh(gioitinh);
            nv.setDiachi(dc);
            nv.setTaikhoan(taikhoan);
            nv.setMatkhau(matkhau);
            if (NhanVienDAO.capnhatNhanVien(nv)) {
                loadNhanVien();
                JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
                btnLuu.setEnabled(false);
                btnThem.setEnabled(true);
                btnXoa.setEnabled(true);
                btnSua.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this, "Somethings went wrong!");
            }

        } else {
            NhanVienDAO.themNhanVien(maBP, tenHinh, ten, sdt, ngaysinh, gioitinh, dc, taikhoan, matkhau, selectedItem);
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loadNhanVien();

        }
    }//GEN-LAST:event_btnLuuActionPerformed

