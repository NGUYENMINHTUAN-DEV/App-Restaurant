    private void loadNhanVien() {
        ArrayList<NhanVien> dsNV = NhanVienDAO.layDanhSachNhanVien(txtTimKiem.getText());
        dtmNV = (DefaultTableModel) tbDSNV.getModel();
        String[] header = {"Mã nhân viên", "Họ tên", "Giới tính", "Số điện thoại", "Địa chỉ", "Ngày sinh", "Bộ phận", "Hình", "Tài Khoản", "Mật Khẩu", "Quyền"};
        dtmNV.setColumnIdentifiers(header);
        dtmNV.setRowCount(0);
        for (NhanVien nv : dsNV) {
            Vector<Object> v = new Vector<>();
            v.add(nv.getMaNV());
            v.add(nv.getHoTen());
            v.add(nv.getGioitinh());
            v.add(nv.getSdt());
            v.add(nv.getDiachi());
            v.add(nv.getNgaysinh());
            v.add(nv.getTenBP());
            v.add(nv.getHinh());
            v.add((nv.getTaikhoan()));
            v.add(nv.getMatkhau());
            v.add(nv.getQuyen());
            dtmNV.addRow(v);
        }
        TableColumnModel tcm = tbDSNV.getColumnModel();
        tcm.removeColumn(tcm.getColumn(7));
        tbDSNV.setModel(dtmNV);
        hienThiChiTiet(txtMaNV.getText());
    }

    private void loadBoPhan() {
        ArrayList<BoPhan> dsBP = BOPHANDAO.layDanhSachBoPhan();
        dcmBP = (DefaultComboBoxModel) cboBoPhan.getModel();
        dcmBP.removeAllElements();
        for (BoPhan bp : dsBP) {
            Vector<Object> v = new Vector<>();
            v.add(bp.getMaBP());
            v.add(bp.getTenBP());
            dcmBP.addElement(bp);
        }
        cboBoPhan.setModel(dcmBP);
    }
    private void loadQ()
    {
         cbo_quyen.removeAllItems();
        for(String q:NhanVienDAO.getDSQ())
            cbo_quyen.addItem(q);

    }
    private void loadQuyen() {
        ArrayList<NhanVien> dsNV = NhanVienDAO.layDanhSachNhanVien(maNV);
        dcmNV = (DefaultComboBoxModel) cbo_quyen.getModel();
        dcmNV.removeAllElements();
        for (NhanVien nv : dsNV) {
            Vector<Object> v = new Vector<>();
            v.add(nv.getQuyen());
            dcmNV.addElement(nv);
        }
        cbo_quyen.setModel(dcmNV);
    }
   
