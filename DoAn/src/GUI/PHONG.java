    public void laydulieuphong(ArrayList<POJO.Phong> dssp){
        tblData.removeAllElements();
        for(POJO.Phong b : dssp){
            Vector v = new Vector();
            v.add(b.getMaPhong()); v.add(b.getTenPhong()); v.add(b.getTrangThai());v.add(b.getSoLuongKhachToiDa());v.add(b.getDonGiaThue());
            tblData.add(v);
        }
        
        jTable1.setModel(new DefaultTableModel(tblData, tblTitl3e));
    }
