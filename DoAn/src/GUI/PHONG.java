    public Boolean KiemTraTXT(){
        if( txtTenPhong.getText().equals("")|| txtTrangThai.getText().equals("")|| txtSoLuong.getText().equals("")|| txtDonGia.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin", "Thông báo", 2);
            return false;
        }
        return true;
    }
    
  
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
         if(KiemTraTXT() ){
            if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn thêm không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                String sql = "insert into Phong(TenPhong, SoLuongKhachToiDa, DonGiaThue, TrangThai) values ( N'"+txtTenPhong.getText().trim()+"', '"+txtSoLuong.getText().trim()+"', '"+txtDonGia.getText().trim()+"', N'"+txtTrangThai.getText().trim()+"')";
                int kq=PhongDAO.ThemXoaSuaPhong(sql);
                if(kq!=-1)
                {
                    dsp = PhongDAO.getDP();
                    laydulieuphong(dsp);
               
              
                }
                else
                     JOptionPane.showMessageDialog(this, "Mã phòng này đã tồn tại, không thể thêm", "Thông báo", 2);
                    
            }
        }      
        
    }//GEN-LAST:event_btnThemActionPerformed

